

public class Throw {

    private int numberOfPins;

    public Throw()
    {
        numberOfPins = 0;
    }

    public Throw(int n)
    {
        numberOfPins = n;
    }

    public int getPins()
    {
        return numberOfPins;
    }

    public void setPins(int n)
    {
        numberOfPins = n;
    }
}
